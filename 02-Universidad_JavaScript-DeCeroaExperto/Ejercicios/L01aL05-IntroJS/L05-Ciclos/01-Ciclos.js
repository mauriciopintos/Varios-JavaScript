// While
let contador = 0;

while(contador < 3){
    console.log(contador);
    contador++;
}
console.log(contador);


// Do While
contador = 0;

do{
    console.log(contador);
    contador++;
}while(contador < 3);
console.log(contador);


// For
for(let contador = 0; contador < 3; contador++) {
    console.log(contador);
}
console.log('Fin ciclo for');
